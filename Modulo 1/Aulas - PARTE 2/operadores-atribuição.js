/*
    

Operadores de atribuição

= -> Atribuição

+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto


*/


let number = 20
number += 5
console.log(number);

let firstNumber = 23
firstNumber -= 10
console.log(firstNumber);

let number2 = 30
number2 /= 5
console.log(number2);

let numberSecond = 25
numberSecond *= 2
console.log(numberSecond);

let number3 = 33
number3 %= 2
console.log(number3);