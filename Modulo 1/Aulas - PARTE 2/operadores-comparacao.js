/*
    Operadores de Comparação

    == -> Igual, ele compara o valor,mas não compara o tipo do valor
    === -> TOTALMENTE igual, ele verifica o tipo do dado! -> SEMPRE USAR ESSE OPERADOR DE COMPARAÇAO

    !=  -> Diferente,ele compara o valor,mas não compara o tipo do valor
    !== -> TOTALMENTE diferente, ele verifica o tipo do dado!  -> SEMPRE USAR ESSE OPERADOR DE COMPARAÇAO

*/