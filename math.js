/*
    Math - Ferramenta do JS

    OBS: todas ferramentas abaixo são uma função

*/

console.log(Math.PI); // Mostra o valor do PI
console.log(Math.pow(2,5)); // Permite usarmos potenciação - primeiro numero a base e segundo expoente
console.log(Math.sqrt(25)); // Calcular a raiz de um número
console.log(Math.random()); // Mostra número aleatorio entre 0 e 1
console.log(Math.ceil(23.6)); // Arredonda um número para cima
console.log(Math.floor(23.6)); // Arredonda um número pra baixo